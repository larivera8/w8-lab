#problem 2 is writing a function that takes two inputs and see if they are greater than 10, less than 10, or equal to 10.

x = int(input("enter number: "))
y = int(input("enter an additional number: "))
z = 10
if x + y == z:
    print("The number is equal to 10")

if x + y < z:
    print("Number is less than 10")

if x + y > z:
    print("Number is greater than 10")
